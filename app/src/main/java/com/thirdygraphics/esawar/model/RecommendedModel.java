package com.thirdygraphics.esawar.model;

public class RecommendedModel {

    int pic;
    String name;


    public RecommendedModel(int pic, String name) {
        this.pic = pic;
        this.name = name;

    }

    public int getPic() {
        return pic;
    }

    public String getName() {
        return name;
    }



}

package com.thirdygraphics.esawar.model;

public class HomepagePlacesModel {
    String places;
    String image;

    public HomepagePlacesModel(String places, String image) {
        this.places = places;
        this.image = image;
    }

    public String getPlaces() {
        return places;
    }

    public String getImage() {
        return image;
    }

}
